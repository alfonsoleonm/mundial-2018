                <h1>Resumen</h1>
                <p>
                    La Celeste, mejor defensa de la fase de grupos, sin ningún gol en contra, aguantó este sábado en
                    Sochi ante Portugal, y se medirá con Francia en cuartos.
                    <p>
                        <p>

                            El gol inicial de Edinson Cavani en los primeros compases, a pase de Luis Suárez, puso el choque muy
                            de cara para los uruguayos, que pudieron permitirse entonces dejar que Portugal atacase. El cuadro
                            luso se limitó a merodear la sólida retaguardia contraria, esperando una aparición de Cristiano
                            Ronaldo, pero su gran figura no llegó a ver puerta, ni siquiera con un golpe franco que parecía
                            idóneo para él.
                        </p>
                        <p>
                            Con todo, los campeones de Europa equilibraban a balón parado, con un cabezazo del defensa Pepe en
                            un saque de esquina en el tramo inicial del segundo tiempo. De nada les serviría, puesto que Cavani
                            sentenció conectando luego un sensacional tiro cruzado. Uruguay aspirará a mostrar esta misma
                            solidez defensiva contra el ataque de Francia, que ganó un poco antes a Argentina. La cita será el 6
                            de julio en Nizhni Nóvgorod.
